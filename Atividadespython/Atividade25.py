#Calcule o valor da hipotenusa de um triângulo
# informacao
cateto_a = float(input("Informe o valor do cateto A: "))
cateto_b = float(input("Informe o valor do cateto B: "))
# Cálculo da hipotenusa usando o teorema de Pitágoras
hipotenusa = (cateto_a*2 + cateto_b*2)
# Resultado
print("O valor da hipotenusa é:", hipotenusa)